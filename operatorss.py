#Arithmetic Operators
print("5+6 is:",5+6)
print("5-6 is:",5-6)
print("5*6 is:",5*6)
print("5/6 is:",5/6)
print("5//6 is:",5//6)
print("5%5 is:",5%5)
print("\n\n")
#Assignment Operators
x=5
x+=7
print(x)
y=8
y-=7
print(y)
print("\n\n")
#Comparison Operators
i=8
print(i==5)
print(i!=3)
print(i>=7)
print(i<=3)
print("\n\n")
#Logical Operators
a=True
b=False
print(a and b)
print(a or b)
print(a is b)
print(a is not b)
print("\n\n")
#Membership Operators
list=[1,2,3,4,5,6,7,8,9]
print(10 in list)
print(4 in list)
print(12 not in list)
print(3 not in list)
print("\n\n")
#Bitwise Operators
a1=True
b1=False
print(a & b)
print(a | b)
print("\n\n")




